"""Shared numeric primitives with no upward dependencies.

Importing this module from both ``benchmarks/`` and ``scripts/benchmark/``
is safe — it pulls in nothing from the rest of the benchmarks package.
"""

from __future__ import annotations

import math


def percentile(values: list[float], p: float) -> float:
    if not values:
        return 0.0
    if p <= 0:
        return min(values)
    if p >= 100:
        return max(values)
    ordered = sorted(values)
    rank = (len(ordered) - 1) * (p / 100.0)
    lo = math.floor(rank)
    hi = math.ceil(rank)
    if lo == hi:
        return ordered[lo]
    weight = rank - lo
    return ordered[lo] * (1.0 - weight) + ordered[hi] * weight
